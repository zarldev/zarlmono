func TestSyntaxChunkerMetadataPreserved(t *testing.T) {
	src := `package p
func Foo(a int) (int, error) {
	if a > 0 {
		return a * 2, nil
	}
	return 0, nil
}
`
	chunker := retrieval.SyntaxChunker{}
	chunks, err := chunker.Chunk(t.Context(), []retrieval.Document{{
		ID:       "annotated.go",
		Text:     src,
		Metadata: retrieval.Metadata{"ext": ".go", "source": "testdata"},
	}})
	if err != nil {
		t.Fatal(err)
	}
	if len(chunks) != 1 {
		t.Fatalf("got %d chunks, want 1", len(chunks))
	}
	c := chunks[0]
	if c.Metadata["source"] != "testdata" {
		t.Errorf("source metadata not preserved: %v", c.Metadata["source"])
	}
	if c.Metadata["parent_id"] != "annotated.go" {
		t.Errorf("parent_id = %q", c.Metadata["parent_id"])
	}
	if c.ID == "" {
		t.Error("chunk ID is empty")
	}
}

func TestSyntaxChunkerReceiverNames(t *testing.T) {
	tests := []struct {
		src, wantReceiver string
	}{
		{`package p
type T struct{}
func (t T) M() {}
`, "T"},
		{`package p
type T struct{}
func (t *T) M() {}
`, "*T"},
	}
	chunker := retrieval.SyntaxChunker{}
	for i, tt := range tests {
		chunks, err := chunker.Chunk(t.Context(), []retrieval.Document{{
			ID:   "recv.go",
			Text: tt.src,
		}})
		if err != nil {
			t.Fatal(err)
		}
		var recv string
		for _, c := range chunks {
			if c.Metadata["kind"] == "method" {
				recv, _ = c.Metadata["receiver"].(string)
			}
		}
		if recv != tt.wantReceiver {
			t.Errorf("test %d: receiver = %q, want %q", i, recv, tt.wantReceiver)
		}
	}
}

