func TestSyntaxChunkerGroupedTypes(t *testing.T) {
	// Grouped type blocks must chunk each spec independently without overlap.
	src := "package p\n\ntype (\n\tA struct{ X int }\n\tB struct{ Y int }\n)\n"
	chunker := retrieval.SyntaxChunker{}
	chunks, err := chunker.Chunk(t.Context(), []retrieval.Document{{ID: "group.go", Text: src}})
	if err != nil {
		t.Fatal(err)
	}
	if len(chunks) != 2 {
		t.Fatalf("got %d chunks, want 2", len(chunks))
	}
	if chunks[0].Metadata["name"] != "A" {
		t.Errorf("chunk 0 name = %q", chunks[0].Metadata["name"])
	}
	if chunks[1].Metadata["name"] != "B" {
		t.Errorf("chunk 1 name = %q", chunks[1].Metadata["name"])
	}
	// Chunk 0 must not contain chunk 1's source.
	if strings.Contains(chunks[0].Text, "B struct") {
		t.Errorf("chunk 0 overlaps chunk 1:\n%s", chunks[0].Text)
	}
	// The full source must be covered: chunk 0 + chunk 1 should cover the
	// original text from the "type" keyword to the closing paren.
	fullText := chunks[0].Text + chunks[1].Text
	if !strings.Contains(fullText, "type (") || !strings.Contains(fullText, ")") {
		t.Errorf("combined chunks don't cover the grouped block:\nA:\n%s\nB:\n%s", chunks[0].Text, chunks[1].Text)
	}
}
