func TestSyntaxChunkerGoFiles(t *testing.T) {
	t.Run("chunks functions and types", func(t *testing.T) {
		src := `package main

func hello() string {
	return "hello"
}

type Greeter struct {
	Name string
}

func (g *Greeter) Greet() string {
	return "hello " + g.Name
}

type Namer interface {
	Name() string
}
`
		chunker := retrieval.SyntaxChunker{}
		chunks, err := chunker.Chunk(t.Context(), []retrieval.Document{{
			ID:   "main.go",
			Text: src,
		}})
		if err != nil {
			t.Fatal(err)
		}

		if len(chunks) != 4 {
			t.Fatalf("got %d chunks, want 4", len(chunks))
		}

		// Check chunk kinds and names.
		want := []struct {
			kind, name, receiver string
		}{
			{"func", "hello", ""},
			{"type", "Greeter", ""},
			{"method", "Greet", "*Greeter"},
			{"type", "Namer", ""},
		}
		for i, w := range want {
			c := chunks[i]
			if c.Metadata["kind"] != w.kind {
				t.Errorf("chunk %d: kind = %q, want %q", i, c.Metadata["kind"], w.kind)
			}
			if c.Metadata["name"] != w.name {
				t.Errorf("chunk %d: name = %q, want %q", i, c.Metadata["name"], w.name)
			}
			if w.receiver != "" && c.Metadata["receiver"] != w.receiver {
				t.Errorf("chunk %d: receiver = %q, want %q", i, c.Metadata["receiver"], w.receiver)
			}
			if got := c.Metadata["parent_id"]; got != "main.go" {
				t.Errorf("chunk %d: parent_id = %q, want main.go", i, got)
			}
			start, startOK := c.Metadata["start_line"].(int)
			end, endOK := c.Metadata["end_line"].(int)
			if !startOK || start < 1 {
				t.Errorf("chunk %d: start_line missing or invalid: %v", i, c.Metadata["start_line"])
			}
			if !endOK || end < start {
				t.Errorf("chunk %d: end_line missing or invalid: %v", i, c.Metadata["end_line"])
			}
		}

		// Verify chunk text is proper Go source.
		if chunks[0].Text != `func hello() string {
	return "hello"
}` {
			t.Errorf("chunk 0 text = %q", chunks[0].Text)
		}
		if chunks[3].Text != "type Namer interface {\n\tName() string\n}" {
			t.Errorf("chunk 3 text = %q", chunks[3].Text)
		}
	})

	t.Run("empty file returns no chunks", func(t *testing.T) {
		chunker := retrieval.SyntaxChunker{}
		chunks, err := chunker.Chunk(t.Context(), []retrieval.Document{{
			ID:   "empty.go",
			Text: "",
		}})
		if err != nil {
			t.Fatal(err)
		}
		if len(chunks) != 0 {
			t.Fatalf("got %d chunks, want 0", len(chunks))
		}
	})

	t.Run("skips forward declarations", func(t *testing.T) {
		src := `package main
func External() // no body
func Local() int { return 1 }
`
		chunker := retrieval.SyntaxChunker{}
		chunks, err := chunker.Chunk(t.Context(), []retrieval.Document{{
			ID:   "skip.go",
			Text: src,
		}})
		if err != nil {
			t.Fatal(err)
		}
		if len(chunks) != 1 {
			t.Fatalf("got %d chunks, want 1 (forward decl skipped)", len(chunks))
		}
		if chunks[0].Metadata["name"] != "Local" {
			t.Errorf("name = %q, want Local", chunks[0].Metadata["name"])
		}
	})

	t.Run("falls back for non-Go files", func(t *testing.T) {
		src := "def foo():\n    pass\n"
		chunker := retrieval.SyntaxChunker{Size: 4}
		chunks, err := chunker.Chunk(t.Context(), []retrieval.Document{{
			ID:   "script.py",
			Text: src,
		}})
		if err != nil {
			t.Fatal(err)
		}
		if len(chunks) == 0 {
			t.Fatal("fallback should have produced chunks")
		}
	})

	t.Run("falls back on parse error", func(t *testing.T) {
		chunker := retrieval.SyntaxChunker{}
		chunks, err := chunker.Chunk(t.Context(), []retrieval.Document{{
			ID:   "broken.go",
			Text: "package main\nfunc {",
		}})
		if err != nil {
			t.Fatal(err)
		}
		if len(chunks) != 1 {
			t.Fatalf("got %d chunks from fallback, want 1", len(chunks))
		}
	})

	t.Run("generated file is skipped", func(t *testing.T) {
		src := `// Code generated by foo; DO NOT EDIT.
package main

func GeneratedFunc() int { return 42 }
`
		chunker := retrieval.SyntaxChunker{}
		chunks, err := chunker.Chunk(t.Context(), []retrieval.Document{{
			ID:   "gen.go",
			Text: src,
		}})
		if err != nil {
			t.Fatal(err)
		}
		if len(chunks) != 0 {
			t.Fatalf("got %d chunks, want 0 (generated file skipped)", len(chunks))
		}
	})

	t.Run("generated file not skipped when SkipGenerated=false", func(t *testing.T) {
		src := `// Code generated by foo; DO NOT EDIT.
package main

func GeneratedFunc() int { return 42 }
`
		chunker := retrieval.SyntaxChunker{
			Languages:     []string{"go"},
			SkipGenerated: false,
		}
		chunks, err := chunker.Chunk(t.Context(), []retrieval.Document{{ID: "gen.go",
			Text: src,
		}})
		if err != nil {
			t.Fatal(err)
		}
		if len(chunks) != 1 {
			t.Fatalf("got %d chunks, want 1 (SkipGenerated=false)", len(chunks))
		}
	})

	t.Run("language from metadata", func(t *testing.T) {
		chunker := retrieval.SyntaxChunker{
			Languages: []string{"python"},
			Size:      4,
		}
		chunks, err := chunker.Chunk(t.Context(), []retrieval.Document{{
			ID:       "script.txt",
			Text:     "def foo():\n    pass\n",
			Metadata: retrieval.Metadata{"language": "python"},
		}})
		if err != nil {
			t.Fatal(err)
		}
		if len(chunks) == 0 {
			t.Fatal("should have produced chunks via language match")
		}
	})

	t.Run("chunker interface compile-time check", func(t *testing.T) {
		var c retrieval.Chunker = &retrieval.SyntaxChunker{}
		_ = c
	})
}

