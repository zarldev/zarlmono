const jsonNull = "null"

// eventTypeAssistant and eventTypeStreamEvent are the two Claude Code
// stream-json line types the parsers below switch on: the terminal per-turn
// snapshot, and the incremental streamed delta wrapper.
const (
	eventTypeAssistant   = "assistant"
	eventTypeStreamEvent = "stream_event"
)

// toolCallTypeFunction is the OpenAI-style tool-call discriminator the
// provider stamps on every emitted tool call.
const toolCallTypeFunction = "function"

const (
	defaultModel   = "sonnet"
	defaultTimeout = 10 * time.Minute
)

// Token is a long-lived Claude Code OAuth token generated by
// `claude setup-token`. The Claude Code CLI consumes it through the
// CLAUDE_CODE_OAUTH_TOKEN environment variable.
type Token struct {
	Access  string
	Expires time.Time
}

// TokenSource returns the Claude Code OAuth token to expose to the
// Agent SDK / Claude Code CLI subprocess.
type TokenSource interface {
	Token(ctx context.Context) (Token, error)
}

// StaticTokenSource is useful for tests and one-off callers.
type StaticTokenSource struct {
	T Token
}

// Token returns the fixed token T as-is; it never refreshes or
// re-derives it, so expiry handling is the caller's problem.
func (s StaticTokenSource) Token(_ context.Context) (Token, error) { return s.T, nil }

// Provider implements llm.Provider by invoking the official Claude Code
// Agent SDK CLI surface (`claude -p`). This intentionally uses Claude
// Code's supported OAuth path instead of treating a subscription token as
// an Anthropic API key.
type Provider struct {
	tokens     TokenSource
	model      string
	binaryPath string
	timeout    time.Duration
}

// NewProvider builds a Claude Code-backed provider. tokens is required —
// it supplies the OAuth token the CLI subprocess authenticates with. The
// defaults (model "sonnet", binary "claude" from PATH, 10-minute timeout)
// are overridable via WithModel / WithBinaryPath / WithTimeout.
func NewProvider(tokens TokenSource, opts ...options.Option[Provider]) (*Provider, error) {
	if tokens == nil {
		return nil, errors.New("claudecode: TokenSource is required")
	}
	p := &Provider{
		tokens:     tokens,
		model:      defaultModel,
		binaryPath: "claude",
		timeout:    defaultTimeout,
	}
	for _, opt := range opts {
		opt(p)
	}
	return p, nil
}

// WithModel selects the model alias or full model id passed to the CLI's
// --model flag. Empty keeps the default ("sonnet").
func WithModel(model string) options.Option[Provider] {
	return func(p *Provider) {
		if model != "" {
			p.model = model
		}
	}
}

// WithBinaryPath overrides where the claude binary is found. Empty keeps
// the default PATH lookup of "claude".
func WithBinaryPath(path string) options.Option[Provider] {
	return func(p *Provider) {
		if path != "" {
			p.binaryPath = path
		}
	}
}

// WithTimeout bounds a single completion subprocess run. Non-positive
// values keep the 10-minute default.
func WithTimeout(timeout time.Duration) options.Option[Provider] {
	return func(p *Provider) {
		if timeout > 0 {
			p.timeout = timeout
		}
	}
}

// Name returns the registered provider id — the "claude-code" wire
// string of llm.LLMProviders.CLAUDECODE.
func (p *Provider) Name() string { return llm.LLMProviders.CLAUDECODE.String() }

// Complete generates a completion as a native iter.Seq2 — the stream's
// chunks are yielded directly (errors as the second value), no channel.
func (p *Provider) Complete(ctx context.Context, req llm.CompletionRequest) (iter.Seq2[llm.CompletionChunk, error], error) {
	return func(yield func(llm.CompletionChunk, error) bool) {
		stopped := false
		safeYield := func(chunk llm.CompletionChunk, err error) bool {
			if stopped {
				return false
			}
			if !yield(chunk, err) {
				stopped = true
				return false
			}
			return true
		}
		if err := p.run(ctx, req, safeYield); err != nil && !stopped {
			safeYield(llm.CompletionChunk{Done: true, FinishReason: "error"}, err)
		}
	}, nil
}

func (p *Provider) run(ctx context.Context, req llm.CompletionRequest, yield func(llm.CompletionChunk, error) bool) error {
	tok, err := p.tokens.Token(ctx)
	if err != nil {
		return fmt.Errorf("claudecode: fetch token: %w", err)
	}
	if tok.Access == "" {
		return errors.New("claudecode: empty OAuth token")
	}

	prompt := buildPrompt(req)
	if strings.TrimSpace(prompt) == "" {
		return errors.New("claudecode: empty prompt")
	}

	runCtx := ctx
	cancel := func() {}
	if p.timeout > 0 {
		runCtx, cancel = context.WithTimeout(ctx, p.timeout)
	}
	defer cancel()

	args := []string{
		"-p",
		"--output-format", "stream-json",
		"--verbose",
		"--include-partial-messages",
		"--no-session-persistence",
		"--tools", "",
	}
	if p.model != "" {
		args = append(args, "--model", p.model)
	}
	cmd := exec.CommandContext(runCtx, p.binaryPath, args...)
	// The prompt grows with conversation length (rendered history, tool
	// results) and a long conversation pushes it past Linux's per-argument
	// MAX_ARG_STRLEN (128KiB), which fails the exec with an argument-list
	// error the CLI surfaces as a parameter-count complaint. Stdin has no
	// such ceiling.
	cmd.Stdin = strings.NewReader(prompt)
	cmd.Env = append(os.Environ(),
		"CLAUDE_CODE_OAUTH_TOKEN="+tok.Access,
		// Ensure API keys in the caller environment do not take precedence
		// over the subscription OAuth token this provider was selected for.
		"ANTHROPIC_API_KEY=",
		"ANTHROPIC_AUTH_TOKEN=",
	)
	stdout, err := cmd.StdoutPipe()
	if err != nil {
		return fmt.Errorf("claudecode: stdout pipe: %w", err)
	}
	stderr, err := cmd.StderrPipe()
	if err != nil {
		return fmt.Errorf("claudecode: stderr pipe: %w", err)
	}
	if err := cmd.Start(); err != nil {
		return fmt.Errorf("claudecode: start %q: %w", p.binaryPath, err)
	}
	stderrDone := readAllAsync(stderr)
	toolCallState := newToolCallState()
	usage, ok, parseErr := parseStream(stdout, yield, toolCallState, len(req.Tools) > 0)
	waitErr := cmd.Wait()
	stderrText := strings.TrimSpace(string(<-stderrDone))
	// The consumer broke / cancelled mid-stream; don't yield or report a
	// terminal subprocess error. Range-over-func panics if iteration continues
	// after yield returns false, and cmd.Wait may report the cancellation that
	// was caused by that same early stop.
	if !ok {
		return nil
	}
	if parseErr != nil {
		return parseErr
	}
	if waitErr != nil {
		if stderrText != "" {
			return fmt.Errorf("claudecode: claude exited: %w: %s", waitErr, stderrText)
		}
		return fmt.Errorf("claudecode: claude exited: %w", waitErr)
	}
	yield(llm.CompletionChunk{Done: true, FinishReason: "stop", Usage: usage}, nil)
	return nil
}

func readAllAsync(r io.Reader) <-chan []byte {
	ch := make(chan []byte, 1)
	go func() {
		b, _ := io.ReadAll(r)
		ch <- b
	}()
	return ch
}

func buildPrompt(req llm.CompletionRequest) string {
	var b strings.Builder
	for _, m := range req.Messages {
		role := strings.TrimSpace(m.Role)
		if role == "" {
			role = llm.RoleUser
		}
		content := messageText(m)
		if m.Role == llm.RoleTool {
			if content == "" {
				continue
			}
			id := strings.TrimSpace(m.ToolCallID)
			if id == "" {
				fmt.Fprintf(&b, "<tool_result>\n%s\n</tool_result>\n\n", content)
			} else {
				fmt.Fprintf(&b, "<tool_result tool_call_id=%q>\n%s\n</tool_result>\n\n", id, content)
			}
			continue
		}
		if content == "" && len(m.ToolCalls) == 0 {
			continue
		}
		fmt.Fprintf(&b, "<%s>\n%s\n</%s>\n\n", role, content, role)
		if len(m.ToolCalls) > 0 {
			fmt.Fprintf(&b, "<assistant_tool_calls>\n%s\n</assistant_tool_calls>\n\n", renderToolCalls(m.ToolCalls))
		}
	}
	if len(req.Tools) > 0 {
		raw, _ := json.Marshal(req.Tools)
		b.WriteString("<available_tools>\n")
		b.Write(raw)
		b.WriteString("\n</available_tools>\n\n")
		b.WriteString(
			"Tool calling protocol: when you need one of these tools, respond with exactly one JSON object and no prose: {\"tool_calls\":[{\"id\":\"call_unique\",\"name\":\"tool_name\",\"arguments\":{...}}]}. If no tool is needed, answer normally.\n\n",
		)
	}
	b.WriteString(responseFormatDirective(req.ResponseFormat))
	return b.String()
}

// renderToolCalls serializes prior assistant tool calls into the same flat
// {id, name, arguments} shape the tool-calling protocol directive documents,
// with arguments as a live JSON object rather than a stringified blob.
//
// The transport ToolCall is the OpenAI nesting (a "function" wrapper whose
// "arguments" is a JSON-*encoded string*). Echoing that verbatim into the
// prompt taught the model a format that contradicts the directive, and the
// model — priming on the rendered history over the instruction — copied the
// double-stringified arguments and routinely fumbled the inner escaping,
// producing invalid JSON that toolparse can't recover, so the call leaked
// back as visible text. Rendering the documented shape removes the conflict
// and the escaping hazard; toolparse.ParseArtifact recovers it either way.
func renderToolCalls(calls []llm.ToolCall) string {
	type flatCall struct {
		ID        string          `json:"id"`
		Name      string          `json:"name"`
		Arguments json.RawMessage `json:"arguments"`
	}
	out := make([]flatCall, 0, len(calls))
	for _, c := range calls {
		args := strings.TrimSpace(c.Function.Arguments)
		if args == "" || !json.Valid([]byte(args)) {
			args = "{}"
		}
		out = append(out, flatCall{
			ID:        c.ID,
			Name:      c.Function.Name,
			Arguments: json.RawMessage(args),
		})
	}
	raw, err := json.Marshal(out)
	if err != nil {
		return "[]"
	}
	return string(raw)
}

// responseFormatDirective is claudecode's best-effort honoring of
// ResponseFormat. The claude CLI can't grammar-constrain output the way the
// API providers do (no schema flag), but Claude reliably follows an explicit
// JSON directive, so we append one. JSON-schema mode embeds the schema
// verbatim. Empty for the unconstrained text default.
func responseFormatDirective(rf llm.ResponseFormat) string {
	switch rf.Type {
	case llm.ResponseFormatJSONObject:
		return "Respond with a single valid JSON object and nothing else — no prose, no markdown fences.\n\n"
	case llm.ResponseFormatJSONSchema:
		schema, err := json.Marshal(rf.Schema)
		if err != nil || rf.Schema.IsZero() {
			return "Respond with a single valid JSON object and nothing else — no prose, no markdown fences.\n\n"
		}
		return "Respond with a single valid JSON object that conforms to this JSON Schema, and nothing else — no prose, no markdown fences:\n" +
			string(schema) + "\n\n"
	default:
		return ""
	}
}

func messageText(m llm.Message) string {
	if len(m.Parts) == 0 {
		return m.Content
	}
	var b strings.Builder
	for _, p := range m.Parts {
		if p.Type == llm.ContentTypeText && p.Text != "" {
			if b.Len() > 0 {
				b.WriteByte('\n')
			}
			b.WriteString(p.Text)
		}
	}
	if b.Len() == 0 {
		return m.Content
	}
	return b.String()
}

// parseStream scans the CLI's stream-json output and yields chunks. The
// returned bool is false when yield returned false (consumer broke /
// cancelled), so the caller skips the terminal chunk rather than yielding
// past a false return.
func parseStream(r io.Reader, yield func(llm.CompletionChunk, error) bool, tools *toolCallState, toolProtocol bool) (*llm.Usage, bool, error) {
	scanner := bufio.NewScanner(r)
	scanner.Buffer(make([]byte, 0, 64*1024), 1024*1024)
	var lastText string
	var lastThinking string
	var buffered strings.Builder
	var usage *llm.Usage
	for scanner.Scan() {
		line := strings.TrimSpace(scanner.Text())
		if line == "" {
			continue
		}
		// The terminal "result" event (and per-turn assistant events) carry
		// token usage; capture the latest so the result event's final totals
		// win. Lines without usage leave it unchanged.
		if u := usageFromLine(line); u != nil {
			usage = u
		}
		if calls := tools.toolCallsFromLine(line); len(calls) > 0 {
			if !yield(llm.CompletionChunk{ToolCalls: calls}, nil) {
				return usage, false, nil
			}
		}
		if thinking := thinkingDeltaFromLine(line); thinking != "" {
			delta := thinking
			if strings.HasPrefix(thinking, lastThinking) {
				delta = thinking[len(lastThinking):]
				lastThinking = thinking
			} else {
				lastThinking += thinking
			}
			if delta != "" {
				if !yield(llm.CompletionChunk{Thinking: delta}, nil) {
					return usage, false, nil
				}
			}
		}
		text := textDeltaFromLine(line)
		if text == "" {
			continue
		}
		if toolProtocol {
			if strings.HasPrefix(text, lastText) && len(text) >= len(lastText) {
				buffered.WriteString(text[len(lastText):])
				lastText = text
			} else {
				buffered.WriteString(text)
				lastText += text
			}
			continue
		}
		// Claude Code partial messages contain the whole current text, while
		// stream_event text_delta lines contain only the delta. When the line
		// is a cumulative snapshot (prefix of, or equal to, what we've seen),
		// emit just the new suffix — which is empty for the terminal
		// `assistant` event that merely repeats the full text the deltas
		// already produced. Emitting that empty suffix (rather than falling
		// through) is what stops the whole message being duplicated.
		if strings.HasPrefix(text, lastText) {
			delta := text[len(lastText):]
			lastText = text
			if delta == "" {
				continue
			}
			if !yield(llm.CompletionChunk{Content: delta}, nil) {
				return usage, false, nil
			}
			continue
		}
		lastText += text
		if !yield(llm.CompletionChunk{Content: text}, nil) {
			return usage, false, nil
		}
	}
	if err := scanner.Err(); err != nil {
		return usage, true, fmt.Errorf("claudecode: read stream: %w", err)
	}
	if toolProtocol {
		if calls := parseToolProtocol(buffered.String()); len(calls) > 0 {
			if !yield(llm.CompletionChunk{ToolCalls: calls}, nil) {
				return usage, false, nil
			}
		} else if buffered.Len() > 0 {
			if !yield(llm.CompletionChunk{Content: buffered.String()}, nil) {
				return usage, false, nil
			}
		}
	}
	return usage, true, nil
}

// usageFromLine extracts token usage from a Claude Code stream-json line.
// The terminal "result" event carries the authoritative totals under
// "usage"; assistant message events carry a per-turn "usage" we fall back
// to. Field names follow the Anthropic usage shape (input_tokens,
// output_tokens, cache_{creation,read}_input_tokens); a line without a
// recognizable usage object yields nil, so a CLI version that diverges just
// leaves usage unreported rather than wrong. PromptTokens sums base +
// cache-creation + cache-read to match the anthropic provider.
func usageFromLine(line string) *llm.Usage {
	var m map[string]any
	if json.Unmarshal([]byte(line), &m) != nil {
		return nil
	}
	raw, _ := m["usage"].(map[string]any)
	if raw == nil {
		if msg, _ := m["message"].(map[string]any); msg != nil {
			raw, _ = msg["usage"].(map[string]any)
		}
	}
	if raw == nil {
		return nil
	}
	intf := func(k string) int {
		if v, ok := raw[k].(float64); ok {
			return int(v)
		}
		return 0
	}
	cacheRead := intf("cache_read_input_tokens")
	prompt := intf("input_tokens") + intf("cache_creation_input_tokens") + cacheRead
	out := intf("output_tokens")
	if prompt == 0 && out == 0 {
		return nil
	}
	return &llm.Usage{
		PromptTokens:     prompt,
		CompletionTokens: out,
		TotalTokens:      prompt + out,
		CachedTokens:     cacheRead,
	}
}

func parseToolProtocol(text string) []llm.ToolCall {
	res := toolparse.ParseArtifact(text)
	return res.Calls
}

type partialToolCall struct {
	id        string
	name      string
	arguments string
	seeded    bool // arguments came whole from content_block_start's input
}

type toolCallState struct {
	byIndex map[int]partialToolCall
	emitted map[string]bool
}

func newToolCallState() *toolCallState {
	return &toolCallState{
		byIndex: map[int]partialToolCall{},
		emitted: map[string]bool{},
	}
}

func (s *toolCallState) toolCallsFromLine(line string) []llm.ToolCall {
	var m map[string]any
	if err := json.Unmarshal([]byte(line), &m); err != nil {
		return nil
	}
	if m["type"] == eventTypeStreamEvent {
		return s.toolCallsFromStreamEvent(m)
	}
	if m["type"] == eventTypeAssistant {
		if msg, _ := m["message"].(map[string]any); msg != nil {
			return s.toolCallsFromContent(msg["content"])
		}
		return s.toolCallsFromContent(m["content"])
	}
	return nil
}

func (s *toolCallState) toolCallsFromStreamEvent(m map[string]any) []llm.ToolCall {
	ev, _ := m["event"].(map[string]any)
	if ev == nil {
		return nil
	}
	idx := intValue(ev["index"])
	switch ev["type"] {
	case "content_block_start":
		cb, _ := ev["content_block"].(map[string]any)
		if cb == nil || cb["type"] != "tool_use" {
			return nil
		}
		pt := partialToolCall{
			id:   stringValue(cb["id"]),
			name: stringValue(cb["name"]),
		}
		if pt.id == "" {
			pt.id = fmt.Sprintf("tool_%d", idx)
		}
		if raw, ok := cb["input"]; ok && raw != nil {
			if b, err := json.Marshal(raw); err == nil && string(b) != jsonNull && string(b) != "{}" {
				pt.arguments = string(b)
				pt.seeded = true
			}
		}
		s.byIndex[idx] = pt
	case "content_block_delta":
		delta, _ := ev["delta"].(map[string]any)
		if delta == nil || delta["type"] != "input_json_delta" {
			return nil
		}
		pt, exists := s.byIndex[idx]
		if !exists {
			pt = partialToolCall{id: fmt.Sprintf("tool_%d", idx)}
			s.byIndex[idx] = pt
		}
		if pt.seeded {
			// A backend that put the full input on content_block_start AND
			// streams input_json_delta would otherwise concatenate the two
			// into invalid JSON. Deltas are the streaming source of truth —
			// drop the seed and rebuild from the deltas.
			pt.arguments = ""
			pt.seeded = false
		}
		pt.arguments += stringValue(delta["partial_json"])
		s.byIndex[idx] = pt
	case "content_block_stop":
		pt, exists := s.byIndex[idx]
		if !exists || pt.name == "" {
			return nil
		}
		delete(s.byIndex, idx)
		return s.emit(pt)
	}
	return nil
}

func (s *toolCallState) toolCallsFromContent(v any) []llm.ToolCall {
	parts, ok := v.([]any)
	if !ok {
		return nil
	}
	var calls []llm.ToolCall
	for i, part := range parts {
		pm, _ := part.(map[string]any)
		if pm == nil || pm["type"] != "tool_use" {
			continue
		}
		pt := partialToolCall{
			id:   stringValue(pm["id"]),
			name: stringValue(pm["name"]),
		}
		if pt.id == "" {
			pt.id = fmt.Sprintf("tool_%d", i)
		}
		if raw, ok := pm["input"]; ok && raw != nil {
			if b, err := json.Marshal(raw); err == nil && string(b) != jsonNull {
				pt.arguments = string(b)
			}
		}
		calls = append(calls, s.emit(pt)...)
	}
	return calls
}

func (s *toolCallState) emit(pt partialToolCall) []llm.ToolCall {
	if pt.id == "" || pt.name == "" || s.emitted[pt.id] {
		return nil
	}
	s.emitted[pt.id] = true
	args := strings.TrimSpace(pt.arguments)
	if args == "" {
		args = "{}"
	}
	return []llm.ToolCall{{
		ID:   pt.id,
		Type: toolCallTypeFunction,
		Function: llm.ToolCallFunction{
			Name:      pt.name,
			Arguments: args,
		},
	}}
}

func stringValue(v any) string {
	s, _ := v.(string)
	return s
}

func intValue(v any) int {
	switch n := v.(type) {
	case int:
		return n
	case int64:
		return int(n)
	case float64:
		return int(n)
	default:
		return 0
	}
}

func textDeltaFromLine(line string) string {
	var m map[string]any
	if err := json.Unmarshal([]byte(line), &m); err != nil {
		return ""
	}
	if m["type"] == eventTypeStreamEvent {
		if ev, _ := m["event"].(map[string]any); ev != nil {
			if delta, _ := ev["delta"].(map[string]any); delta != nil && delta["type"] == "text_delta" {
				if s, _ := delta["text"].(string); s != "" {
					return s
				}
			}
		}
	}
	if m["type"] == eventTypeAssistant {
		if msg, _ := m["message"].(map[string]any); msg != nil {
			return contentText(msg["content"])
		}
		return contentText(m["content"])
	}
	return ""
}

func contentText(v any) string {
	parts, ok := v.([]any)
	if !ok {
		return ""
	}
	var b strings.Builder
	for _, part := range parts {
		pm, _ := part.(map[string]any)
		if pm == nil {
			continue
		}
		if pm["type"] == "text" {
			if s, _ := pm["text"].(string); s != "" {
				b.WriteString(s)
			}
		}
	}
	return b.String()
}

// thinkingDeltaFromLine extracts extended-thinking text the same way
// textDeltaFromLine extracts visible text — from stream_event
// content_block_delta lines (delta.type == "thinking_delta") and from the
// terminal assistant snapshot's "thinking" content blocks. Without this, the
// model's reasoning is parsed as nothing and silently disappears.
func thinkingDeltaFromLine(line string) string {
	var m map[string]any
	if err := json.Unmarshal([]byte(line), &m); err != nil {
		return ""
	}
	if m["type"] == eventTypeStreamEvent {
		if ev, _ := m["event"].(map[string]any); ev != nil {
			if delta, _ := ev["delta"].(map[string]any); delta != nil && delta["type"] == "thinking_delta" {
				if s, _ := delta["thinking"].(string); s != "" {
					return s
				}
			}
		}
	}
	if m["type"] == eventTypeAssistant {
		if msg, _ := m["message"].(map[string]any); msg != nil {
			return thinkingText(msg["content"])
		}
		return thinkingText(m["content"])
	}
	return ""
}

func thinkingText(v any) string {
	parts, ok := v.([]any)
	if !ok {
		return ""
	}
	var b strings.Builder
	for _, part := range parts {
		pm, _ := part.(map[string]any)
		if pm == nil {
			continue
		}
		if pm["type"] == "thinking" {
			if s, _ := pm["thinking"].(string); s != "" {
				b.WriteString(s)
			}
		}
	}
	return b.String()
}
